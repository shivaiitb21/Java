// operator pehle hoga to printed value jo hogi vo new value hogi operation ke bad
// operator bad me hoga to printed value jo hogi vo purani hi ayegi but uspe operation hua hoga
public class incremental_decremental_operators {

	public static void main(String[] args) {
		
		int a=10;
		System.out.println(a--); //10 --> 9 ab iska value 9 hoga
		System.out.println(--a); //9 --> 8
		System.out.println(++a); //8 --> 9
		System.out.println(++a); //9 --> 10

	}

}
